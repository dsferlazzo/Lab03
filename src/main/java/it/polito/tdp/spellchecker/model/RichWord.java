package it.polito.tdp.spellchecker.model;

public class RichWord {
	
	String parola;
	Boolean corretta;
	
	public RichWord(String parola, Boolean corretta) {
		super();
		this.parola = parola;
		this.corretta = corretta;
	}

	public String getParola() {
		return parola;
	}

	public Boolean getCorretta() {
		return corretta;
	}
	
	
	
	
	
	

}
